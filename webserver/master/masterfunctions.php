function humanTime($playtime) {
  if ($playtime > 60) {
    $hours = $playtime / 60;
    if ($hours > 24) {
      $days = $hours / 24;
      if ($days > 365) {
        $years = $days / 365;
        return round($years, 2)." Jahre";
      } else {
        return round($days, 2)." Tage";
      }

    } else {
      return round($hours, 2)." h";
    }
  } else {
    return $playtime." min";
  }
}

